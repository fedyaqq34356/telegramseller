    """–ü–æ–ª—É—á–∏—Ç—å —Å–æ–æ–±—â–µ–Ω–∏–µ –Ω–∞ –Ω—É–∂–Ω–æ–º —è–∑—ã–∫–µ —Å —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ–º"""
    message = MESSAGES.get(lang, MESSAGES['ru']).get(key, MESSAGES['ru'][key])
    if kwargs:
        return message.format(**kwargs)
    return message